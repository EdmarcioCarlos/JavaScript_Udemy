//Operadores Comparação

//igualdade Solta ((dado)
console.log(3 == '3')
// igualdade Estrita (dado e tipo iguais)
console.log(3 === '3')
// Desigualde solta (dado)
console.log(3 != 3)
// Desigualde Estrita (dado e tipo iguais)
console.log(3 !== 3)

console.log(8 > 5) // Maior que
console.log(5 < 5) // Menor que
console.log(5 >= 5) // Maior ou igual
console.log(5 <= 5) //Menor ou igual
